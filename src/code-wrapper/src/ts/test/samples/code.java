// Class1 is a class
public class Class1{
  // static1 is a static method
  public static void static1(){}

  // Class1 is a constructor
  public Class1(){}

  // method1 is a method 
  public void method1(){}
}

// Class2 is a class
public class Class2 implements Interface1{
  // interfaceMethod1 is an implementation of an interface method
  public void interfaceMethod1(){}
}

// Interface1 is an interface
public interface Interface1{
  // interfaceMethod1 is an interface method
  public void interfaceMethod1();
}

// Enum1 is an Enum 
public enum Enum1{
  // ENUM1 is an enum constant
  ENUM1,
  // ENUM2 is an enum constant
  ENUM2;
}
